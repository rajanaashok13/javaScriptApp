//OPERATORS IN JAVASCRIPT
//ARITHMETIC OPERATORS
let a = 10;
let b = 20;
console.log("addition ", a + b);
console.log("subtraction ", a - b);
console.log("multiplication ", a * b);
console.log("remainder ", a % b);
console.log("division ", a / b);
// Tricy
console.log(a + b + "hii");
console.log("hii" + a + b);
console.log("20" - "10");
console.log("20" * "10");
console.log("20" / "2");
console.log("20" / 2);
console.log("30" / "hii"); // NaN not a number
// assignment operators or comparison
let c = 20; // Literal synatax
console.log(b == c);
console.log(b === c);
let d = new Number(20); // objecr syntax
console.log(b == d);
console.log(b === d);
